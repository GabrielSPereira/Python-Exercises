# Questão 1. Faça um programa que receba três notas, calcule e mostre a média 
# aritmética entre elas. Exiba, as notas, e a respectiva média.

N1 = float(input("Digite a nota 1\n"))
N2 = float(input("Digite a nota 2\n"))
N3 = float(input("Digite a nota 3\n"))
media = (N1 + N2 + N3)/3
print("Nota 1 é ",N1,", Nota 2 é ",N2,"e a nota 3 é ",N3,". Sendo a média ",media)
